grade = 82
# You can run as many elif's as you need.  The first one to execute will
# be the only one to run.  The others will be skipped.
# They go in the order that they are listed.
if grade >= 90:
    print("You got an A!")
elif grade >= 80:
    print("You got an B!")
elif grade >= 70:
    print("You got C!")
elif grade >= 65:
    print("You got D!")
else:
    print("You sucked at this class!")